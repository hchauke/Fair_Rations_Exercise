package com.fairRations.tdd;

import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);

        int N = in.nextInt();

        int B[] = new int[N];

        for(int B_i=0; B_i < N; B_i++){
            B[B_i] = in.nextInt();
        }
    }
}